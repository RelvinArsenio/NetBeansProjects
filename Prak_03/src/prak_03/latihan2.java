    private void bUkuranActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bUkuranActionPerformed
        // TODO add your handling code here:
        if (bUkuran.getSelectedItem() == "11") {
            jTextArea1.setFont(new Font(jTextArea1.getFont().getName(), jTextArea1.getFont().getStyle(), 14));
        } else if (bUkuran.getSelectedItem() == "12") {
            jTextArea1.setFont(new Font(jTextArea1.getFont().getName(), jTextArea1.getFont().getStyle(), 16));
        } else if (bUkuran.getSelectedItem() == "14") {
            jTextArea1.setFont(new Font(jTextArea1.getFont().getName(), jTextArea1.getFont().getStyle(), 38));
        }
    }//GEN-LAST:event_bUkuranActionPerformed

    private void bWarnaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bWarnaActionPerformed
        // TODO add your handling code here:
        if (bWarna.getSelectedItem() == "Hitam") {
            jTextArea1.setForeground(Color.BLACK);
        } else if (bWarna.getSelectedItem() == "Biru") {
            jTextArea1.setForeground(Color.BLUE);
        } else if (bWarna.getSelectedItem() == "Magenta") {
            jTextArea1.setForeground(Color.MAGENTA);
        }
    }//GEN-LAST:event_bWarnaActionPerformed

