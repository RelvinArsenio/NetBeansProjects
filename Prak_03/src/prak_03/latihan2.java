    private void bFontActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bFontActionPerformed
        // TODO add your handling code here:
        if (bFont.getSelectedItem() == "Segoe UI") {
            jTextArea1.setFont(new Font("Segoe UI", Font.PLAIN, 11));
        } else if (bFont.getSelectedItem() == "Arial") {
            jTextArea1.setFont(new Font("Arial", Font.PLAIN, 11));
        } else if (bFont.getSelectedItem() == "Comic Sans MS") {
            jTextArea1.setFont(new Font("Comic Sans MS", Font.PLAIN, 11));
        }
    }//GEN-LAST:event_bFontActionPerformed

