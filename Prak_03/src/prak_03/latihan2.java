    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        Font tebal = new Font("Segoe UI", Font.BOLD, 11);
        Font biasa = new Font("Segoe UI", Font.PLAIN, 11);
        if (rbLaki.isSelected()) {
            if (jCheckBox1.isSelected()) {
                jTextArea1.setText(" Nama : " + txtnama.getText() + ", Jenis Kelamin : " + rbLaki.getText());
                jTextArea1.setFont(tebal);
            } else {
                jTextArea1.setText(" Nama : " + txtnama.getText() + ", Jenis Kelamin : " + rbLaki.getText());
                jTextArea1.setFont(biasa);
            }
        } else if (rbPerempuan.isSelected()) {
            if (jCheckBox1.isSelected()) {
                jTextArea1.setText(" Nama : " + txtnama.getText() + ", Jenis Kelamin :" + rbPerempuan.getText());
                jTextArea1.setFont(tebal);
            } else {
                jTextArea1.setText(" Nama : " + txtnama.getText() + ", Jenis Kelamin :" + rbPerempuan.getText());
                jTextArea1.setFont(biasa);
            }
        }
    }//GEN-LAST:event_jButton1MouseClicked

